		<h1>
			A Year in Films: 2011
		</h1>
		
		<p class="intro">
			It's been a pretty good year in films. I managed to see <strong>124</strong> of them this year. Here's two lists of my favourites: one is for new releases,
			and the other is for films released before 2011. Enjoy.
		</p>
		
		<div class="fb-like" data-href="http://www.craigmunro.net/best-of/2011/" data-send="false" data-width="260" data-show-faces="false"></div>		
		
		<ul class="new">
			<li>
				<h2>Top ten released this year</h2>
			</li>
			<li>
				<img src="films/10.jpg"/>
				<h3>
					10. Scream 4
					<span>Dir. Wes Craven</span>
				</h3>
				<p class="first">
					One of two movies that I chose which I wouldn't expect to be on anyone else's top ten list (the other being number 9).
					The return of the Wes Craven/Kevin Williamson franchise was something I had be anticipating all year.
				</p>
				
				<p>
					The first three Scream movies are among some of the most-watched titles in my collection, and I relished
					the prospect of adding a fourth movie to my shelves. It failed to match the wit and sparkle of the first three,
					though it did contain an accurate parody of our increasing obsession with fame.
				</p>			
			</li>
			
			<li>
				<img src="films/9.jpg"/>
				<h3>
					9. Warrior
					<span>Dir. Gavin O'Connor</span>
				</h3>
				<p class="first">
					The first major Hollywood movie about MMA, the fastest growing sport in the world.
				</p>
				
				<p>
					Mixed Martial Arts (MMA) hasn't yet managed to become a major name in sports in the UK just yet,
					though expect that to change. I've caught the MMA bug several years ago, and have even recently
					started training at an MMA academy. It's a big deal. The movie was a whirlwind mix of every ingredient
					required in a movie about fighting &mdash; feuding brothers (who'll (obviously) eventually meet in the ring); an alcoholic
					father; philosophical posturing about what a man truly is. Cliched, but in the perfect way.
				</p>
			</li>
			
			<li>
				<img src="films/8.jpg"/>
				<h3>
					8. Tree of Life
					<span>Dir. Terence Malick</span>
				</h3>
				<p class="first">
					A film that is probably on every top ten list this year, and one which absolutely deserves its place.
				</p>
				<p>
					On my appearance on the Splendor Cinema Podcast I failed to articulate what I thought about the movie. To
					a large degree I still do. It's a work that you'll either love as an important, true expression of emotion from a master
					of the craft, or hate it as a pompous, over-religious, possibly sexist piece of work.
					I think it's all of those things, and I absolutely love it.
				</p>
			</li>
			
			<li>
				<img src="films/7.jpg"/>
				<h3>
					7. Page One: Inside the New York Times
					<span>Dir. Andrew Rossi</span>
				</h3>
				<p class="first">
					An inside look at the death rattle of the printed news industry.
				</p>
				<p>
					Printed newspapers are dying. Circulations are dwindling, and the newsmen of yesteryear are finding it hard
					to find relevance in the modern world. This film covers interesting ground on why it's important that newspapers
					stay around, how they might find relevance, and how they're integrating modern technologies into their
					workflow and worldview.
				</p>
			</li>
			
			<li>
				<img src="films/6.jpg"/>
				<h3>
					6. 13 Assassins
					<span>Dir. Takashi Miike</span>
				</h3>
				<p class="first">
					The most accessible and emotional piece of film-making I've seen from Miike.
				</p>
				<p>
					Never have I felt such visceral hatred about a movie villain than in 13 Assassins. Lord Naritsugu Matsudaira, and the story of his
					eventual takedown by 13 warriors is one that I won't forget.
				</p>
			</li>
			
			<li>
				<img src="films/5.jpg"/>
				<h3>
					5. Barney's Version
					<span>Dir. Richard J. Lewis</span>
				</h3>
				<p class="first">
					Hoffman. Giamatti. Pike. Boom.
				</p>
				<p>
					Who doesn't like Paul Giamatti? It's hard to find anything wrong with anything he's ever done on screen, and that includes his role
					in the massively misunderstood Lady In The Water.
					I don't normally like to give props to movie adaptations of books, but this one really got to me. A great story of mistakes, loss, and regret
					that's easy to recommend, even if the movie did steal a rather major plot point from Magnolia.
				</p>
			</li>
			
			<li>
				<img src="films/4.jpg"/>
				<h3>
					4. Snow Town
					<span>Dir. Justin Kurzel</span>
				</h3>
				<p class="first">
					So good, I never want to watch it again.
				</p>
				<p>
					I don't plan on rewatching three of my top four movies from this year, and it's all for the same reason. They're nasty. They depict the worst aspects
					of normality, the darkness that surrounds it, and the horror that lurks within. Snow Town is the story of a serial killer you'll find hard to hate.
				</p>
			</li>
			
			<li>
				<img src="films/3.jpg"/>
				<h3>
					3. The Ides of March
					<span>Dir. George Clooney</span>
				</h3>
				<p class="first">
					&ldquo;It doesn't matter what you thought. It matters what you did. It matters what you didn't do.&rdquo;
				</p>
				<p>
					There's a real lack of good movies about politics being produced in the UK (no, The Kings Speech does not count). We're exceptionally good at social commentary,
					and can create satire at will, but we seem incapable or unwilling to make cinema that talks about politics in any way other than absurdist
					(see In The Loop, Four Lions). Ides is played straight, and contains a dark realisation without resorting to parody.
					It also marks the first on-screen pairing of the two most-loved heavyweight character actors, Paul Giamatti and Phillip Seymour Hoffman.
				</p>
			</li>
			
			<li>
				<img src="films/2.jpg"/>
				<h3>
					2. Tyrannosaur
					<span>Dir. Paddy Considine</span>
				</h3>
				<p class="first">
					Peter Mullan retreads old ground, but creates something sharper than his previous work.
				</p>
				<p>
					Peter Mullan received huge praise for his role in Paddy Considine's first movie. It's a great performance, but while watching Tyrannosaur I couldn't get My Name Is Joe, the Ken Loach movie,
					out of my head. Tyrannosaur owes a lot to the somewhat superior Ken Loach movie in terms of plot, characterisation, cinematic technique, and tone, but it has a darker, more stylised edge,
					which I loved.
				</p>
			</li>
			
			<li>
				<img src="films/1.jpg"/>
				<h3>
					1. Kill List
					<span>Dir. Ben Wheatley</span>
				</h3>
				<p class="first">
					A deeply unsettling film, and another which hints at a horrible underworld lurking under the veneer of society.
				</p>
				<p>
					This year seems to be dominated by films with a pessimistic outlook.
					This is the best of the bunch, though, as it not only has a completely unforgiving, bleak ending, but expertly mixes motifs from horror movies like Rosemary's Baby,
					The Wicker Man, with Loachian, naturalistic acting and cinematography.
				</p>			
			</li>
		</ul>
	
